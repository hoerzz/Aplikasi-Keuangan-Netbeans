    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        try {
            //File file = new File("src/Laporan/LapGajiKaryawanSesuaiJenis.jrxml");
            //jasDes = JRXmlLoader.load(file);
            InputStream file = getClass().getResourceAsStream("/Laporan/LapGajiKaryawan.jrxml");
            jasDes = JRXmlLoader.load(file);
            SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
            String nkaryawan = n_karyawan.getSelectedItem().toString();;
            param.put("n_karyawan", nkaryawan);
            jasRep = JasperCompileManager.compileReport(jasDes);
            jasPri = JasperFillManager.fillReport(jasRep,param,new Fungsi.Koneksi().konek);
            JasperViewer.viewReport(jasPri,false);
        } catch (Exception e) {
            System.out.println(""+e);
        }
    }//GEN-LAST:event_btnCariActionPerformed

