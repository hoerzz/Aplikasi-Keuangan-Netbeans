    private void DataKaryawan()
    {
        try {
          PreparedStatement prestat = Koneksi.konek.prepareStatement("SELECT nama_karyawan FROM datagajikaryawan");
          ResultSet rs = prestat.executeQuery();
          n_karyawan.removeAllItems();
          
          while(rs.next())
          {
              String name = rs.getString("nama_karyawan");
              n_karyawan.addItem(name);
          }
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }

